package week01.expressions_and_control_flow.types;

public class TwoNumbers {
    public static void main(String[] args) {
        int num1, num2;
        num1 = 22;
        num2 = 13;

        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println((double) num1/num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2);

    }
}
