package week01.expressions_and_control_flow.variables;

public class DefineBasicInfo {
    public static void main(String[] args) {
        String myName = "Ákos Balázs";
        int myAge = 28;
        double myHeight = 1.82;
        boolean marriedOrNot = false;

        System.out.println("My name is "+myName);
        System.out.println("My name age is "+myAge);
        System.out.println("My name height is "+myHeight);
        System.out.println("The statement that I am married is "+marriedOrNot);
        // Define several things as a variable then print their values
        // Your name as a string
        // Your age as an integer
        // Your height in meters as a double
        // Whether you are married or not as a boolean
    }
}
